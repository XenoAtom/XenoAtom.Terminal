namespace XenoAtom.Terminal;

/// <summary>
/// Specifies which mouse button is involved in a mouse event.
/// </summary>
public enum TerminalMouseButton
{
    /// <summary>
    /// No button.
    /// </summary>
    None,

    /// <summary>
    /// Left button.
    /// </summary>
    Left,

    /// <summary>
    /// Right button.
    /// </summary>
    Right,

    /// <summary>
    /// Middle button.
    /// </summary>
    Middle,

    /// <summary>
    /// Extra button 1.
    /// </summary>
    X1,

    /// <summary>
    /// Extra button 2.
    /// </summary>
    X2,

    /// <summary>
    /// Mouse wheel.
    /// </summary>
    Wheel
}

